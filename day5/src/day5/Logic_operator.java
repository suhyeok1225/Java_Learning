package day5;

public class Logic_operator {

	public static void main(String[] args) {
		boolean a=true;
		boolean b=false;
		boolean c=true;
		System.out.println(a&&b);
		System.out.println(a&&c);
		System.out.println((2<4) && (4<5));
		
		System.out.println(a||b);
		System.out.println(a||c);
		System.out.println(c||b);
		System.out.println(!a);
		
				

	}

}

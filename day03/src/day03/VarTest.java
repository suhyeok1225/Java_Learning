package day03;

public class VarTest {

	public static void main(String[] args) {
		int i = 100;
		//정수형 변수
		float f = 3.14f;
		//실수형 변수
		double d = 3.14;
		char c = 'c';
		//문자형 변수
		String s = "hello";
		//문자열 변수
		boolean b = true;
		//논리형 변수
		System.out.println(i);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(s);
		System.out.println(b);
		
		

	}

}
